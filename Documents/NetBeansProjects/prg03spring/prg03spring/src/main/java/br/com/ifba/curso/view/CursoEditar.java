    public void setCurso(Curso curso) {
        
        this.cursoParaEdicao = curso; // Atribui o curso recebido a variável de instância
        
        // Preenche os campos da tela com os dados do curso
        if (cursoParaEdicao != null) {
            txtNome.setText(cursoParaEdicao.getNome());
            txtCodigo.setText(cursoParaEdicao.getCodigoCurso());
            txtCoordenador.setText(cursoParaEdicao.getCoordenador());
            
            if (cursoParaEdicao.isAtivo()) { 
                cbbDisponibilidade.setSelectedItem("Ativo");
            } else {
                cbbDisponibilidade.setSelectedItem("Inativo");
            }
        }
    }
