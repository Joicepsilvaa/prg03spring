    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
         if (cursoParaEdicao != null) {
            // Atualiza o objeto cursoParaEdicao com os dados da tela
            cursoParaEdicao.setNome(txtNome.getText());
            cursoParaEdicao.setCodigoCurso(txtCodigo.getText());
            cursoParaEdicao.setCoordenador(txtCoordenador.getText());
            
            String selectedAvailability = (String) cbbDisponibilidade.getSelectedItem();
            cursoParaEdicao.setAtivo("Ativo".equals(selectedAvailability)); // Define true se "Ativo", false se "Inativo"

            try {
                // Chama o método de atualização no controller
                cursoController.update(cursoParaEdicao); 
                JOptionPane.showMessageDialog(this, "Curso atualizado com sucesso!");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Erro ao atualizar curso: " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Nenhum curso selecionado para edição.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed


